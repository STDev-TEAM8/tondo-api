package com.tondo.api.infrastructure.aws.bedrock.template

/**
 * Bedrock(AI) 호출 시 사용되는 프롬프트 문자열을 생성하는 팩토리 객체.
 * 도메인 파라미터를 받아 AI에 최적화된 프롬프트 포맷을 반환합니다.
 */
object BedrockPromptTemplate {

    /**
     * 도슨트 리포트 프롬프트 생성
     */
    fun createDocentPrompt(
        averageHz: Double,
        averageTimbre: Double,
        averageVolume: Double,
        hexColor: String = "#97b6e1" // 필요시 동적으로 주입할 수 있도록 기본값 파라미터화
    ): String {
        return """
            당신은 미디어 아트 페스티벌의 수석 도슨트입니다. 아래의 관람객 음성 추출 데이터와 이 파동을 시각화하기 위해 AI에 적용된 아트워크 스타일과 실제 적용된 이미지를 바탕으로 도슨트 해설을 작성해 주세요.

            [관람객 음성 분석 데이터]
            - 평균 음고 (형태 결정): ${averageHz}Hz
            - 음색 중심 (굴곡 결정): ${averageTimbre}
            - 목소리 크기 (에너지): ${averageVolume}dB

            [시각화 적용 스타일 및 색상]
            - 메인 색상 코드: ${hexColor} (이 색상을 기반으로 어두움과 밝음이 배치됨)
            - 렌더링 기법: 캔버스 질감이 살아있는 정교한 아크릴화
            - 세부 묘사: 대담하고 대칭적인 구조, 미세한 점들을 선으로 재해석한 이분법적 점묘 기법

            이 데이터를 바탕으로 관람객의 목소리가 어떻게 이 매혹적인 아크릴 작품으로 탄생했는지 도슨트 해설을 작성해주세요. 인삿말과 서론을 제외하고, 세부 묘사와 표현 기법에 대한 설명도 제외해줘.
        """.trimIndent()
    }

    /**
     * 이미지 생성용 프롬프트 생성
     */
    fun createImageGenerationPrompt(
        hexColor: String = "#97b6e1" // 필요시 동적으로 주입
    ): String {
        return """
            [주제 및 데이터 설명]
            "제공된 클라드니 패턴 이미지의 모든 세부 구조와 형태를 절대적으로 유지하십시오."

            [스타일 및 색상 정보 적용]
            "이 패턴을 정교한 아크릴화 작품으로 만드세요. 캔버스 질감과 미세한 아크릴 붓 터치를 사용하여 패턴을 형성하십시오. 제공된 색상 코드 중 어두운 색조는 패턴의 어두운 부분에 할당하고, 밝은 색조는 밝은 패턴 부분에 할당하십시오. 색상은 패턴의 기하학적 형태에 따라 구조적으로 분포해야 하며, 대담하고 활기차게 구성되어야 합니다."

            [용도 및 세부 질감]
            "결과물은 시각적으로 매혹적인 스마트폰 배경화면이어야 합니다. 원본의 미세한 점 패턴을 아크릴 점묘 기법으로 재해석하여 하십시오. 패턴의 미세한 점들은 어두움과 밝음의 이분법적인 구성으로 점이 아닌 선으로 표한하십시오 . 대칭적인 구조가 아크릴 물감의 풍부한 색상과 질감 속에서 완벽하게 조화를 이루게 하십시오."

            [데이터]
            색상코드 = ${hexColor}
        """.trimIndent()
    }
}
